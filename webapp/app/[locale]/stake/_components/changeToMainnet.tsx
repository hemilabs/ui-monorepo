export const ChangeToMainnet = function () {
  const locale = useLocale()
  const [, setNetworkType] = useNetworkType()
  const pathname = usePathname().replace(`/${locale}`, '')
  const t = useTranslations('stake-page')

  const onClick = function (e: MouseEvent<HTMLAnchorElement>) {
    e.preventDefault()
    e.stopPropagation()
    setNetworkType('mainnet')
  }

  return (
    <div className="group/change-to-mainnet relative cursor-pointer">
      <Link
        className="absolute left-0 right-0 z-10 translate-y-1/2 text-lg font-medium text-orange-600 transition-transform group-hover/change-to-mainnet:scale-105"
        href={{ pathname, query: { networkType: 'mainnet' } }}
        onClick={onClick}
      >
        <span className="block text-center">{t('change-to-mainnet')}</span>
      </Link>
      <SvgContainer />
    </div>
  )
}
